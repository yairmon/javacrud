package json;

public class Track {


	String ISBN;
	String name;

	public String getISBN() {
		return ISBN;
	}

	public void setISBN(String iSBN) {
		ISBN = iSBN;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Track [isbn=" + ISBN + ", name=" + name + "]";
	}
}
